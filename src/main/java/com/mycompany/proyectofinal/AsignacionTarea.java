    public void CargarProyecto()
    {
        int IdEquipo = 0;
        
        //Recorrer la lista de equpos para validar si el usuario logeado es lider de algun equipo
        for(Equipo e: ProyectoFinal.Equipos){
            //Encuentra el usuario dque es lider del equipo
            if(e.getLider().equals(this.usuario.getUsuario())){
                IdEquipo = e.getCorrelativo();
                break;
            }
        }
        
        //Creamos un modelo de tipo combo box
        DefaultComboBoxModel<String> modelo = new DefaultComboBoxModel<>();
        
        //Agregamos el elemento inicial que tendra el combo
        modelo.addElement("");
        
        for(AsignacionProyectoEquipo pe: ProyectoFinal.AsignacionProyectoEquipos){
            
            if(pe.getIdEquipo() == IdEquipo){
                //Ciclo para recorrer los equipos que se tienen almacenados
                for(Proyecto eq: ProyectoFinal.Proyectos){
                    if(pe.getIdProyecto() == eq.getCorrelativo()) {
                        //Agregar el equipo como elemento del combo
                        modelo.addElement(eq.getNombreProyecto());
                    }                        
                }
            }
            
        }
        
        //Asignar el modelo ya lleno con los usuarios al combo box de la pantalla
        ProyectoComboBox.setModel(modelo);
    }
    
    public void LlenarIdTarea(){
        int IdEquipo = 0;
        
        //Recorrer la lista de equpos para validar si el usuario logeado es lider de algun equipo
        for(Equipo e: ProyectoFinal.Equipos){
            //Encuentra el usuario dque es lider del equipo
            if(e.getLider().equals(this.usuario.getUsuario())){
                IdEquipo = e.getCorrelativo();
                break;
            }
        }
        
        //Creamos un modelo de tipo combo box
        DefaultComboBoxModel<String> modelo = new DefaultComboBoxModel<>();
        
        //Agregamos el elemento inicial que tendra el combo
        modelo.addElement("");
        
        for(Tarea t: ProyectoFinal.Tareas){  
            for(AsignacionEquipoUsuario eu: ProyectoFinal.AsignacionEquiposUsuarios){ 
                if(eu.getIdEquipo() == IdEquipo){        
                    modelo.addElement(t.getTitulo());
                    break;
                }           
            }
            
        }
        
        TareaComboBox.setModel(modelo);
    }

