    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String TareaL = TareaComboBox.getSelectedItem().toString();
        String ProyectoL = ProyectoComboBox.getSelectedItem().toString();

        //Validar si el combo de equipo no han seleccinado nada
        if(TareaL == "")
        JOptionPane.showMessageDialog(this, "Seleccione una tarea.");
        //Validar si el combo de usuario no han seleccinado nada
        else if(ProyectoL == "")
        JOptionPane.showMessageDialog(this, "Seleccione un proyecto.");

        else {

            int IdTarea = 0;

            //Encontrar Id del proyecto
            for(Tarea p: ProyectoFinal.Tareas){
                if(p.getTitulo().equals(TareaL)){
                    IdTarea = p.getId();
                    break;
                }
            }

            //Validar que el usuario no este asignado a un equipo previamente
            boolean ingresado= false;

            //Recorrer la lista de asgnaciones para validar si el usuario ya esta asignado a un equipo
            for(AsignacionTareaProyecto pe: ProyectoFinal.AsignacionTareasProyetos){
                //Validar si el usuario ya esta asginado
                if(pe.getIdTarea()== IdTarea){
                    ingresado= true;
                    break;
                }
            }

            if(ingresado)
            JOptionPane.showMessageDialog(this, "No se puede guardar porque la tarea ya fue asignada a un proyecto");
            else {

                int IdProyecto = 0;

                //Encontrar Id del proyecto
                for(Proyecto p: ProyectoFinal.Proyectos){
                    if(p.getNombreProyecto().equals(ProyectoL)){
                        IdProyecto = p.getCorrelativo();
                        break;
                    }
                }

                AsignacionTareaProyecto TareaEquipoNuevo = new AsignacionTareaProyecto();
                TareaEquipoNuevo.setIdTarea(IdTarea);
                TareaEquipoNuevo.setIdProyecto(IdProyecto);

                ProyectoFinal.AsignacionTareasProyetos.add(TareaEquipoNuevo);

                TareaComboBox.setSelectedIndex(0);
                ProyectoComboBox.setSelectedIndex(0);

                JOptionPane.showMessageDialog(this, "Tarea asignado al equipo exitosamente");
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

