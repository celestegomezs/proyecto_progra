public class Proyecto {
    private String NombreProyecto;
    private String FechaInicio;
    private String FechaFin;
    private int Correlativo;
    private String Interesado;

    /**
     * @return the NombreProyecto
     */
    public String getNombreProyecto() {
        return NombreProyecto;
    }

    /**
     * @param NombreProyecto the NombreProyecto to set
     */
    public void setNombreProyecto(String NombreProyecto) {
        this.NombreProyecto = NombreProyecto;
    }

    /**
     * @return the FechaInicio
     */
    public String getFechaInicio() {
        return FechaInicio;
    }

    /**
     * @param FechaInicio the FechaInicio to set
     */
    public void setFechaInicio(String FechaInicio) {
        this.FechaInicio = FechaInicio;
    }

    /**
     * @return the FechaFin
     */
    public String getFechaFin() {
        return FechaFin;
    }

    /**
     * @param FechaFin the FechaFin to set
     */
    public void setFechaFin(String FechaFin) {
        this.FechaFin = FechaFin;
    }

    /**
     * @return the Correlativo
     */
    public int getCorrelativo() {
        return Correlativo;
    }

    /**
     * @param Correlativo the Correlativo to set
     */
    public void setCorrelativo(int Correlativo) {
        this.Correlativo = Correlativo;
    }

    /**
     * @return the Interesado
     */
    public String getInteresado() {
        return Interesado;
    }

    /**
     * @param Interesado the Interesado to set
     */
    public void setInteresado(String Interesado) {
        this.Interesado = Interesado;
    }
}
