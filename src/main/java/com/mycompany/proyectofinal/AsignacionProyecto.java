    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         String EquipoL = EquipoComboBox.getSelectedItem().toString();
        String ProyectoL = ProyectoComboBox.getSelectedItem().toString();
        
           
        //Validar si el combo de equipo no han seleccinado nada
        if(EquipoL == "")
            JOptionPane.showMessageDialog(this, "Seleccione un equipo.");
        //Validar si el combo de usuario no han seleccinado nada
        else if(ProyectoL == "")
            JOptionPane.showMessageDialog(this, "Seleccione un proyecto.");
        
         else {
            
            int IdProyecto = 0;
                    
            //Encontrar Id del proyecto
            for(Proyecto p: ProyectoFinal.Proyectos){
                if(p.getNombreProyecto().equals(ProyectoL)){
                    IdProyecto = p.getCorrelativo();    
                    break;
                }
            }
            
            //Validar que el usuario no este asignado a un equipo previamente
            boolean ingresado= false;
            
            //Recorrer la lista de asgnaciones para validar si el usuario ya esta asignado a un equipo
            for(AsignacionProyectoEquipo pe: ProyectoFinal.AsignacionProyectoEquipos){
                //Validar si el usuario ya esta asginado
                if(pe.getIdProyecto() == IdProyecto){
                    ingresado= true;    
                    break;
                }
            }
            
            if(ingresado)
                JOptionPane.showMessageDialog(this, "No se puede guardar porque el proyecto ya fue asignado a un equipo");
              else {
                
                    int IdEquipo = 0;
                    
                    //Encontrar Id del equipo
                    for(Equipo e: ProyectoFinal.Equipos){
                        if(e.getNombreEquipo().equals(EquipoL)){
                            IdEquipo = e.getCorrelativo();    
                            break;
                        }
                    }
                    
                    AsignacionProyectoEquipo EquipoProyectoNuevo = new AsignacionProyectoEquipo();
                    EquipoProyectoNuevo.setIdEquipo(IdEquipo);
                    EquipoProyectoNuevo.setIdProyecto(IdProyecto);

                    ProyectoFinal.AsignacionProyectoEquipos.add(EquipoProyectoNuevo);

                    EquipoComboBox.setSelectedIndex(0);
                    ProyectoComboBox.setSelectedIndex(0);

                    JOptionPane.showMessageDialog(this, "Proyecto asignado al equipo exitosamente");
                }

        
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

